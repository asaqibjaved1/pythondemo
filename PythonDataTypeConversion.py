# Data Type conversion
# Python do type conversion automatically to superior
a = 5
b = 10.5
sum = a + b

print(type(a))
print(type(b))
print(sum)
print(type(sum))

# Type Casting
# When we do conversion manually, it is type casting

c = float("2")
d = 7.5
sum = c + d
print(type(c))
print(type(d))
print(sum)
print(type(sum))
